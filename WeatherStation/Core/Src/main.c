/**
  * @brief  The application entry point.
  * @details This function is the main entry point for a weather station application based on STM32.
  *          It initializes all necessary hardware components and enters an infinite loop to
  *          continuously perform its core functions. These functions include:
  *          - Initializing the HAL library which is responsible for the hardware abstraction layer.
  *          - Setting up system clock for the microcontroller.
  *          - Initializing all configured peripherals such as GPIO, UART, I2C, SPI, and TIM.
  *          - Starting the TIM6 and TIM7 timers in interrupt mode.
  *          - Initializing the ST7735 TFT display and displaying the initial techy interface.
  *          - Initializing the AHT20 sensor for temperature and humidity readings.
  *          - Initializing the SD card for data logging.
  *          - Setting up the ESP module for Wi-Fi communication.
  *          - Entering an infinite loop where the program routinely reads sensor data,
  *            updates the display, fetches weather data via Wi-Fi, and logs data to the SD card.
  *          The routine timings are managed by incrementing counters within the TIM6 interrupt callback.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	FIL fil;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  MX_SPI1_Init();
  MX_SPI2_Init();
  MX_FATFS_Init();
  MX_TIM6_Init();
  MX_USART4_UART_Init();
  MX_USART1_UART_Init();

  /* USER CODE BEGIN 2 */
  ST7735_Init(3);
  AHT20_init();
  SD_init(&fil);
  ESP_init();
  drawTechyInterface();
  SensorData sensor_data;
  WeatherData weather_data;
  ESP_wifiConnect();
  HAL_TIM_Base_Start_IT(&htim6);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
    /* USER CODE BEGIN 3 */
	if (ahtCounter >= AHT_READ_TIME)
	{
		AHT20_read(&sensor_data.temperature, &sensor_data.humidity);
		updateTemperatureAndHumidity(sensor_data.temperature, sensor_data.humidity);
		ahtCounter = 0;
	}
	if (fileWriteCounter >= API_DATA_SAVE_TIME)
	{
		ESP_fetchWeatherData(weather_data.date, weather_data.max_temp, weather_data.min_temp, weather_data.avg_temp, weather_data.weather_condition);
		SD_saveData(&fil, sensor_data.temperature, sensor_data.humidity, weather_data.max_temp, weather_data.min_temp, weather_data.avg_temp, weather_data.weather_condition, weather_data.date);
		fileWriteCounter = 0;
	}
  }
  /* USER CODE END 3 */
}

